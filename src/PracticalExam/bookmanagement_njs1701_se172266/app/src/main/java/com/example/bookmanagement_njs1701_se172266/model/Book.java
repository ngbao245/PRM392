package com.example.bookmanagement_njs1701_se172266.model;

public class Book {
    private long id;
    private String name, type, date;
    private long authorId;

    public Book(String name, String type, String date, long authorId) {
        this.name = name;
        this.type = type;
        this.date = date;
        this.authorId = authorId;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public long getAuthorId() {
        return authorId;
    }

    public void setAuthorId(long authorId) {
        this.authorId = authorId;
    }
}
